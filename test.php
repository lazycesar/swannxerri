Conscience de soi :
Socrate disait : "Connais-toi toi-même." Comme une invitation à la sagesse, au devenir,
au développement personnel.
La conscience de soi est, selon moi, la première étape de n’importe quelle forme de
progrès. Qui je suis, où j’en suis ; mais surtout, où j’ai envie d’aller, qui j’ai envie de
devenir et ce que j’ai envie d’accomplir. Découvrir quelles sont mes motivations
profondes, et comprendre comment me réaliser à mon plein potentiel. Découvrez-vous,
réapprenez à vous connaitre grâce à de nombreux outils, tests de personnalité, tableaux
d’objectifs, outils de visualisation, et bien d’autres choses…

Maîtrise de soi :
Après avoir défini qui je suis et quelle chemin je souhaite entreprendre, il est
important de se sentir prêt et énergique. Cela demande une bonne compréhension et
une meilleure maîtrise de notre corps (véhicule indispensable), ainsi que de notre cerveau
(chef d’orchestre de nos réalisations).
Comment fonctionne le cerveau et est-ce qu’il a des propriétés particulières qui me
permettrait d’être plus efficace ou plus créatif dans ce que j’entreprends ? Vous serez
surpris d’apprendre l’étendue des capacités de votre cerveau lorsque vous en comprenez
son fonctionnement.
N’oublions pas, bien sûr, l’importance de prendre soin de son corps, car il est le véhicule
indispensable qui vous mènera jusqu’au bout de vos projets, idées et aspirations. Quelle
est la place que tient la nutrition, le sommeil, la respiration ou l’exercice physique dans
votre bien-être quotidien ? Maîtriser qui je suis, c’est se stabiliser, c’est trouver l’énergie,
le soutien nécessaire pour se mettre en marche, passer a l’action et créer du
changement. Dans cette partie, je serais accompagné d’un Directeur de recherche au
CNRS, une nutrithérapeute et un coach sportif.

Les clés de la performance individuelle :
Comment je mobilise mon énergie et comment mettre en jeu les atouts nécessaires pour
aller de l’avant, dépasser les obstacles, progresser et devenir plus fort, plus créatif, plus
motivé et efficace, mais aussi plus serein et mieux préparé pour les challenges et les
opportunités de la vie ? Nous apprendrons à transformer nos peurs, à nous libérer de la
pression sociale, mais aussi à nous fixer des objectifs et à prendre confiance en nous-mêmes.

Que ce soit pour réussir professionnellement ou dans notre vie personnelle, les
mécanismes de la performance sont très similaires. C’est ici que je vous donnerai les
étapes menant au succès que vous souhaitez.

Les clés de la performance collective :
S'il est primordial d’avoir un meilleur contrôle de soi et de se sentir prêt à affronter
toutes les situations de la vie, il est tout aussi important de maîtriser notre relation avec
les autres, de savoir s’entourer des bonnes personnes, mais aussi d’être capable d’offrir
à notre entourage ce que l’on a de meilleur : notre présence, notre écoute et notre
bienveillance. Que vous vous sentiez seul en ayant envie de vous ouvrir aux autres, ou si
vous êtes déjà bien entourés mais que vous souhaitez avoir de meilleures relations avec
les autres : dans tous les cas, cette partie correspondra parfaitement à vos attentes.
